    private void footerModifyActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_footerModifyActionPerformed
        // TODO add your handling code here:
        int rowCollege = tableCollege.getSelectedRow();
        int rowCourse  = tableCourse.getSelectedRow();
        if (rowCollege >= 0) {
           framework.editSelectedRow("COLLEGE", tableCollege, "college_code");
        }
        else if (rowCourse >= 0) {
           framework.editSelectedRow("CPROGRAM", tableCourse, "program_code");
        }
    }//GEN-LAST:event_footerModifyActionPerformed

