    private void footerDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_footerDeleteActionPerformed
        // TODO add your handling code here:
        int rowCollege = tableCollege.getSelectedRow();
        if (rowCollege >= 0) {
           framework.softDeleteRow("COLLEGE", tableCollege, "college_code");
        }
        else {
           framework.softDeleteRow("CPROGRAM", tableCourse, "program_code");
        }
    }//GEN-LAST:event_footerDeleteActionPerformed

